fruits = ["Apple", "Peach", "Pear"]

print("Fuit Pie Loop:")
for fruit in fruits:
    print(fruit)
    print(fruit + " pie")

print("\nLoop with Hello Inside")
for fruit in fruits:
    print(fruit)
    print("Hello")

print("\nLoop with Hello Outside")
for fruit in fruits:
    print(fruit)
print("Hello")